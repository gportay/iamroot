#include <stdlib.h>
#include <stdio.h>
#include <string.h>

#include <unistd.h>

int main(int argc, char * const argv[])
{
	gid_t egid;

	if (argc < 2) {
		fprintf(stderr, "Too few arguments\n");
		exit(EXIT_FAILURE);
	} else if (argc > 2) {
		fprintf(stderr, "Too many arguments\n");
		exit(EXIT_FAILURE);
	}

	egid = strtoul(argv[1], NULL, 0);

	if (setegid(egid) == -1) {
		perror("setegid");
		return EXIT_FAILURE;
	}

	return EXIT_SUCCESS;
}
